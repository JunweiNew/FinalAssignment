package com.system.department.entry;

public class Department {
	private int depId;
	private String depName;
	private String depNumber;
	private String depPhone;
	private String depMobile;
	private String depEmail;
	private String depLeader;
	public int getDepId() {
		return depId;
	}
	public void setDepId(int depId) {
		this.depId = depId;
	}
	public String getDepName() {
		return depName;
	}
	public void setDepName(String depName) {
		this.depName = depName;
	}
	public String getDepNumber() {
		return depNumber;
	}
	public void setDepNumber(String depNumber) {
		this.depNumber = depNumber;
	}
	public String getDepPhone() {
		return depPhone;
	}
	public void setDepPhone(String depPhone) {
		this.depPhone = depPhone;
	}
	public String getDepMobile() {
		return depMobile;
	}
	public void setDepMobile(String depMobile) {
		this.depMobile = depMobile;
	}
	public String getDepEmail() {
		return depEmail;
	}
	public void setDepEmail(String depEmail) {
		this.depEmail = depEmail;
	}
	public String getDepLeader() {
		return depLeader;
	}
	public void setDepLeader(String depLeader) {
		this.depLeader = depLeader;
	}
}
